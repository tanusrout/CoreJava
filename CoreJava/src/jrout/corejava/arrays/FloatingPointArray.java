package jrout.corejava.arrays;

public class FloatingPointArray {

	public static void main(String[] args) {
		float[] marks = new float[5];
		marks[0] = 10.5f;
		marks[1] = 20.6f;
		marks[2] = 30.3f;
		
		System.out.println(marks[0]);
		System.out.println(marks[1]);
		System.out.println(marks[4]);
		

	}

}
