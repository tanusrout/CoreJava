package Operators;

public class AdditionOne {

	public static void main(String[] args) {
		int a = 9;
		//a = a+1;
		a++;//we can also use increment operator ++
		System.out.println(a);
		System.out.println(a++);
		
		System.out.println(a++);
		System.out.println(a);
		
		System.out.println(++a);
		System.out.println(a);
	}

}
